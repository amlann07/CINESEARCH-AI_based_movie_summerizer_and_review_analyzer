// Helper function to get related movies (same genre, excluding current)
export const getRelatedMovies = (currentMovie, limit = 4) => {
  return movies
    .filter(movie => 
      movie.id !== currentMovie.id && 
      movie.genres.some(genre => currentMovie.genres.includes(genre))
    )
    .slice(0, limit);
};